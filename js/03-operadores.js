// Operadores aritmeticos

let numeroA = 5;
let numeroB = 2;

console.log(`Numero A: ${numeroA}`);
console.log(`Numero B: ${numeroB}`);

console.log("--------------------");

//Soma
let soma = numeroA + numeroB;
console.log(`Soma é: ${soma}`);

//Subtracao
let sub =  numeroA - numeroB;
console.log(`Subtracao é: ${sub}`);

//Multiplicação
let multi =  numeroA * numeroB;
console.log(`Multiplicação é: ${multi}`);

//Divisão
let div = numeroA / numeroB;
console.log(`Divisão é: ${div}`);

//resto
let resto = numeroA % numeroB;
console.log(`Resto da divisão é: ${div}`);

// operadores de incremento e decremento

let num = 6;
num = num + 1; // num = 7

// operador unario
num++; // num = 8
console.log(num);

// Operador pós fixado

console.log(num++); // num = 8
console.log(num);  // num = 9 , executa a soma num++ após a impressão

// Operador pré fixado

console.log(++num);  // num = 10

// decremento

num--; 
console.log(num);  // num = 9

// operadores de atribuição

num += 1; // num = 10

num += 5; // num = 15

